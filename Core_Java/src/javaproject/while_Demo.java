package javaproject;

import java.util.Scanner;

public class while_Demo {
public static void main(String[]args) {
	
	Scanner sc = new Scanner(System.in);
	int n;
	
	System.out.print("Enter N: ");
	n= sc.nextInt();
	
	while(n>0)	
	   
	   {
		
		System.out.println("Have a nice day!");
		n= n-1;
	   
	   }
	
	
	
}
}
